function [waveform1,mark1,mark2,waveform2,mark3,mark4,waveform3,mark5,mark6,waveform4,mark7,mark8]=CalSSBSequence1(modfCh12,IQScaleCh12,PhasedelayCh12,modfCh34,IQScaleCh34,PhasedelayCh34,mysigma)
amp180=255*2^4;
fixedpoint=44000;
cyclelength=99000;
measuretime=2000;
measuredelay=-80;
cardpretrigger=2000;
cardtriggerlength=100;
numsteps=1;
waveformoffset=2^13;
width=4*mysigma;
QubitPulse=[myw_Pulse('Xs2','amp',amp180,'width',43000)];
[waveform1,waveform2]=myw_GetSeq(QubitPulse,numsteps,fixedpoint,cyclelength,'modf',modfCh12,'IQScale',IQScaleCh12,'phaseError',PhasedelayCh12);
CavityPulse=[myw_Pulse('Xs2','amp',amp180,'width',43000)];
[waveform3,waveform4]=myw_GetSeq(CavityPulse,numsteps,fixedpoint,cyclelength,'modf',modfCh34,'IQScale',IQScaleCh34,'phaseError',PhasedelayCh34);
%--------------------------------------------------------------------------
mark1=[zeros(1,fixedpoint+measuredelay),ones(1,measuretime),zeros(1,cyclelength-fixedpoint-measuredelay-measuretime)];
mark1=repmat(mark1,numsteps,1);
mark2=double(logical(waveform1));
mark2=myw_AddMarkerDelay(mark2);
mark7=[zeros(1,fixedpoint-cardpretrigger),ones(1,cardtriggerlength),zeros(1,cyclelength-(fixedpoint-cardpretrigger)-cardtriggerlength)];
mark7=repmat(mark7,numsteps,1);
mark8=mark2;
mark4=double(logical(waveform3));
mark4=myw_AddMarkerDelay(mark4);
mark3=mark2;
mark6=mark7;
mark5=mark4;
%waveform1=myw_deconvolutionAWG5014(waveform1);
waveform1=round(waveform1)+waveformoffset;
waveform2=round(waveform2)+waveformoffset;
waveform3=round(waveform3)+waveformoffset;
waveform4=round(waveform4)+waveformoffset;